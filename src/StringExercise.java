public class StringExercise {
    public static void main(String[] args) {
        // 1. String Basics
        String input = "We don't need no education\nWe don't need no thought control";
        System.out.println(input + "\n");

        input = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(input + "\n");

        input = "In windows, the main drive is usually C:\\";
        System.out.println(input + "\n");

        input = "I can do backslashes \\, double backslashes \\\\,\nand the amazing triple backslash \\\\\\!";
        System.out.println(input + "\n");
    }
}
